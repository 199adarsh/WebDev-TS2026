import { z } from 'zod';
import { events, sponsors, developers } from './schema';

export const api = {
  events: {
    list: {
      method: 'GET' as const,
      path: '/api/events',
      responses: {
        200: z.array(z.custom<typeof events.$inferSelect>()),
      },
    },
    get: {
      method: 'GET' as const,
      path: '/api/events/:id',
      responses: {
        200: z.custom<typeof events.$inferSelect>(),
        404: z.object({ message: z.string() }),
      },
    },
  },
  sponsors: {
    list: {
      method: 'GET' as const,
      path: '/api/sponsors',
      responses: {
        200: z.array(z.custom<typeof sponsors.$inferSelect>()),
      },
    },
  },
  developers: {
    list: {
      method: 'GET' as const,
      path: '/api/developers',
      responses: {
        200: z.array(z.custom<typeof developers.$inferSelect>()),
      },
    },
  },
};

export function buildUrl(path: string, params?: Record<string, string | number>): string {
  let url = path;
  if (params) {
    Object.entries(params).forEach(([key, value]) => {
      if (url.includes(`:${key}`)) {
        url = url.replace(`:${key}`, String(value));
      }
    });
  }
  return url;
}
